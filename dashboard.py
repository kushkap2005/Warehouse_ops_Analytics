    tiles = [
        ("OUT OF STOCK", str(out_of_stock), C["red"]),
        ("REORDER NOW",  str(reorder_now),  C["orange"]),
        ("LOW STOCK",    str(low_stock),     C["yellow"]),
        ("ALL CLEAR",    str(len(df_all) - out_of_stock - reorder_now - low_stock), C["green"]),
    ]
    ws.row_dimensions[4].height = 10
    for i, (lbl, val, bg) in enumerate(tiles):
        col = 2 + i*2
        kpi_tile(ws, 5, col, lbl, val, bg)

    # Detailed alerts
    alerts = load("reorder_alerts")
    r2 = 9
    banner(ws, r2, "DETAILED REORDER REPORT", 10, bg="8B0000", size=12)
    hcols = [("B","SKU"),("C","Product Name"),("D","Category"),("E","Zone"),
             ("F","Stock"),("G","Reorder Level"),("H","Shortage"),
             ("I","Est. Reorder Cost (₹)"),("J","Alert Level")]
    header_row(ws, r2+1, hcols, bg="8B0000")

    row_bg = {"OUT OF STOCK": "FEE2E2", "REORDER NOW": "FEF3C7", "Low Stock": "FFFBEB"}
    for i, row_data in alerts.iterrows():
        rv = r2 + 2 + list(alerts.index).index(i)
        bg_key = "OUT OF STOCK" if "OUT" in str(row_data.alert_level) else \
                 "REORDER NOW" if "REORDER" in str(row_data.alert_level) else "Low Stock"
        vals = [row_data.sku_id, row_data.product_name, row_data.category,
                row_data.storage_zone, int(row_data.current_stock),
                int(row_data.reorder_level), int(row_data.shortage_units),
                f"₹{row_data.estimated_reorder_cost_inr:,.0f}", row_data.alert_level]
        data_row(ws, rv, vals, bg=row_bg.get(bg_key, "FFFFFF"))
        ws.cell(rv, 10).font = font(bold=True, color=C["red"] if "OUT" in str(row_data.alert_level) else C["orange"])

    # Zone summary
    zone_df = load("reorder_alerts").groupby("storage_zone").agg(
        items_to_reorder=("sku_id","count"),
        total_shortage=("shortage_units","sum"),
        total_cost=("estimated_reorder_cost_inr","sum")
    ).reset_index()

    r3 = r2 + 2 + len(alerts) + 2
    banner(ws, r3, "Reorder Summary by Zone", 5, bg=C["orange"], size=11)
    header_row(ws, r3+1, [("B","Zone"),("C","SKUs to Reorder"),("D","Total Shortage"),("E","Est. Cost (₹)")], bg=C["orange"])
    for i, row_data in zone_df.iterrows():
        rv = r3 + 2 + i
        vals = [row_data.storage_zone, int(row_data.items_to_reorder),
                int(row_data.total_shortage), f"₹{row_data.total_cost:,.0f}"]
        data_row(ws, rv, vals, bg="FFFFFF")



