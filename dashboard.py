def fill(hex_color):
    return PatternFill("solid", start_color=hex_color, fgColor=hex_color)

def font(bold=False, size=11, color="1A1A2E", italic=False, name="Calibri"):
    return Font(bold=bold, size=size, color=color, italic=italic, name=name)

def align(h="left", v="center", wrap=False):
    return Alignment(horizontal=h, vertical=v, wrap_text=wrap)

def thin_border(sides="all"):
    s = Side(style="thin", color="C5D1DC")
    d = {"left": s, "right": s, "top": s, "bottom": s} if sides == "all" else {}
    return Border(**{k: s for k in (sides.split(",") if sides != "all" else d)})

def thick_bottom():
    return Border(bottom=Side(style="medium", color="1B2A4A"))

def set_col_widths(ws, widths):
    for col, w in widths.items():
        ws.column_dimensions[col].width = w

def header_row(ws, row, cols, text_color="FFFFFF", bg=C["navy"], size=11):
    for col, (letter, label) in enumerate(cols, 1):
        c = ws.cell(row=row, column=col, value=label)
        c.font      = font(bold=True, color=text_color, size=size)
        c.fill      = fill(bg)
        c.alignment = align("center")
        c.border    = thin_border()

def data_row(ws, row, values, bg="FFFFFF", bold=False, color="1A1A2E"):
    bg_c = C["light_bg"] if row % 2 == 0 else bg
    for col, val in enumerate(values, 1):
        c = ws.cell(row=row, column=col, value=val)
        c.font      = font(bold=bold, color=color)
        c.fill      = fill(bg_c)
        c.alignment = align("center" if col > 1 else "left")
        c.border    = thin_border()

def banner(ws, row, text, span_end, bg=C["navy"], txt="FFFFFF", size=18, merge=True):
    ws.cell(row=row, column=1, value=text).font = font(bold=True, color=txt, size=size)
    if merge:
        ws.merge_cells(start_row=row, start_column=1, end_row=row, end_column=span_end)
    ws.row_dimensions[row].height = 36
    for col in range(1, span_end+1):
        ws.cell(row=row, column=col).fill = fill(bg)
        ws.cell(row=row, column=col).alignment = align("center")

def kpi_tile(ws, row, col, label, value, bg=C["navy"], val_color="FFFFFF"):
    ws.merge_cells(start_row=row,   start_column=col, end_row=row,   end_column=col+1)
    ws.merge_cells(start_row=row+1, start_column=col, end_row=row+1, end_column=col+1)
    ws.row_dimensions[row].height   = 22
    ws.row_dimensions[row+1].height = 32
    lc = ws.cell(row=row,   column=col, value=label)
    vc = ws.cell(row=row+1, column=col, value=value)
    lc.font = font(size=9, color="B0C4D8", italic=True)
    vc.font = font(bold=True, size=18, color=val_color)
    for r in [row, row+1]:
        for c in range(col, col+2):
            ws.cell(r, c).fill      = fill(bg)
            ws.cell(r, c).alignment = align("center")


